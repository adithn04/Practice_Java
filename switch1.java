public class switch1 {
    public static void main(String[] args) {
        int a = 5;
        switch (a) {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("other");
        }
        System.out.println("bye");
    }
}
